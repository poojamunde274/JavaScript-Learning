console.log("--------------- Swap Two Values ---------------");

var sweety ="Sweety";
var cutie = "Cutie";
console.log("Before Swap Values:", "Sweety's Value Is:", sweety, "Cutie's Value Is:", cutie);

var temp = sweety;
sweety = cutie;
cutie = temp;
console.log("After Swap Values:", "Sweety's Value Is:", sweety, "Cutie's Value Is:", cutie);
